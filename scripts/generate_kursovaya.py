def add_two_column_table(doc: Document, left_header: str, right_header: str, rows: list[tuple[str, str]]) -> None:
    t = doc.add_table(rows=1 + len(rows), cols=2)
    t.rows[0].cells[0].text = left_header
    t.rows[0].cells[1].text = right_header
    for i, (a, b) in enumerate(rows, start=1):
        t.rows[i].cells[0].text = a
        t.rows[i].cells[1].text = b
    doc.add_paragraph()


def code_block(doc: Document, text: str) -> None:
    p = doc.add_paragraph(text)
    p.paragraph_format.line_spacing_rule = WD_LINE_SPACING.SINGLE
    for run in p.runs:
        run.font.name = "Consolas"
        run.font.size = Pt(9)


